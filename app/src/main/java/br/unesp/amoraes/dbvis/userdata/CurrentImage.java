package br.unesp.amoraes.dbvis.userdata;

public class CurrentImage {
	
	private String id;
	private long time;
	private String url;
	
	public String getId() {
		return id;
	}
	public long getTime() {
		return time;
	}
	public String getUrl() {
		return url;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setTime(long time) {
		this.time = time;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
	
	
}
